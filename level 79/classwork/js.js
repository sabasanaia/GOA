function multiply(a, b){
  return(a * b)
}

function evenOrOdd(number) {
  if(number % 2 === 0){
    return("Even")
  }
  else{
    return("Odd")
  }
}

function makeNegative(num) {
  if(num > 0){
    return(num * -1)
  }
  else if(num < 0){
    return(num)
  }
  else{
    return(0)
  }
}

function opposite(number) {
  if(number > 0){
    return(number * -1)
  }
  else if(number < 0){
    return(number * -1)
  }
  else{
    return(0)
  }
}