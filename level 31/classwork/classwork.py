def remove_char(s):
    return s[1:-1]


def bool_to_word(boolean):
    if boolean == True:
        return "Yes"
    else:
        return "No"


def boolean_to_string(b):
    return str(b)


def string_to_number(s):
    return int(s)


def solution(string):
    return string[::-1]


def make_upper_case(s):
    return s.upper()
